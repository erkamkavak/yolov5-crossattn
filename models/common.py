class Attention(nn.Module): 
    def __init__(self, dim, num_heads=8, qkv_bias=False, qk_scale=None, attn_drop=0., proj_drop=0.):
        super().__init__()
        self.dim = dim
        self.num_heads = num_heads
        head_dim = dim // num_heads

        self.scale = qk_scale or head_dim ** -0.5

        self.wq = nn.Linear(dim, dim, bias=qkv_bias)
        self.wk = nn.Linear(dim, dim, bias=qkv_bias)
        self.wv = nn.Linear(dim, dim, bias=qkv_bias)
        self.attn_drop = nn.Dropout(attn_drop)
        self.proj = nn.Linear(dim, dim)
        self.proj_drop = nn.Dropout(proj_drop)
    
    def _reshape_to_heads(self, x): 
        batch_size, seq_len, embed_dim = x.size()
        assert embed_dim == self.dim
        sub_dim = embed_dim // self.num_heads
        return x.view(batch_size, seq_len, self.num_heads, sub_dim).transpose(1, 2)
    
    def _reshape_from_heads(self, x): 
        batch_size, n_head, seq_len, sub_dim = x.size()
        assert n_head == self.num_heads
        out_dim = sub_dim * n_head
        assert out_dim == self.dim
        return x.transpose(1, 2).contiguous().view(batch_size, seq_len, out_dim)
    
    def forward(self, q, k, v):
        # q -> (B, q_seq_len, C)
        # k -> (B, kv_seq_len, C)
        # v -> (B, kv_seq_len, C)
        q = self._reshape_to_heads(self.wq(q)) # (B, nh, q_seq_len, sub_dim)
        k = self._reshape_to_heads(self.wk(k)) # (B, nh, kv_seq_len, sub_dim)
        v = self._reshape_to_heads(self.wv(v)) # (B, nh, kv_seq_len, sub_dim)

        # self-attend: (B, nh, q_seq_len, sub_dim) x (B, nh, sub_dim, kv_seq_len) -> (B, nh, q_seq_len, kv_seq_len)
        attn = (q @ k.transpose(-2, -1)) * self.scale
        attn = attn.softmax(dim=-1)
        attn = self.attn_drop(attn)

        y = attn @ v # (B, nh, q_seq_len, kv_seq_len) x (B, nh, kv_seq_len, sub_dim) -> (B, nh, q_seq_len, sub_dim)
        y = self._reshape_from_heads(y) # re-assemble all head outputs side by side
        # y -> (B, q_seq_len, C)

        y = self.proj_drop(self.proj(y))
        return y


def pad_if_required(x, scale_factor):
    """
    Add padding to the image x if it is not divisible by scale_factor 
    """ 
    B, C, H, W = x.shape

    pad_h = (scale_factor - H % scale_factor) % scale_factor
    pad_w = (scale_factor - W % scale_factor) % scale_factor

    if pad_h > 0 or pad_w > 0:
        x = F.pad(x, (0, pad_w, 0, pad_h, 0, 0))

    return x, (pad_h, pad_w)

def remove_pad(x, pad_to_remove): 
    H, W = pad_to_remove
    x = x[:, :, :H, :W].contiguous()
    return x

class PSDownsampling(nn.Module): 
    def __init__(self, channel, scale_factor=2): 
        super(PSDownsampling, self).__init__()
        self.scale_factor = scale_factor
        self.pixel_unshuffle = nn.PixelUnshuffle(scale_factor) 
        self.compression = nn.Conv2d(channel * scale_factor**2, channel, kernel_size=(1, 1), stride=(1, 1))

    def forward(self, x): 
        # x, added_pad = pad_if_required(x, self.scale_factor)
        x = self.pixel_unshuffle(x) # B, C * scale_factor**2, H//scale_factor, W//scale_factor 
        x = self.compression(x) # B, C, H // scale_factor, W // scale_factor
        return x


